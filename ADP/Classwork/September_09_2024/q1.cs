class Employee {
    private int id;
    private int salary;
    public string? name;

    // using getter and setter in C#
    public int ID {
        get {
            return id;
        }
        set {
            id = value;
        }
    }

    public int SALARY {
        get {
            return salary;
        }
        set {
            salary = value;
        }
    }

    public string? NAME {
        get {
            return name;
        }
        set {
            name = value;
        }
    }
}
